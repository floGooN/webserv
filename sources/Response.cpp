Response::Response(const Request &)
{}

Response::Response(const Response &ref) {
    *this = ref;
}

Response::~Response()
{   }

Response &Response::operator=(const Response &ref)
{
    if (this != &ref)
    {
        _header = ref._header;
        _uriReconstituated = ref._uriReconstituated;
    }
    return *this;
}


